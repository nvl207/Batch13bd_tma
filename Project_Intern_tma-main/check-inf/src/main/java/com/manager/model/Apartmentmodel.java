package com.manager.model;

public class Apartmentmodel {
	private int id_apartment;
	private String name_apartment;
	private String add_apartment;
	private int number_of_floor;
	public int getId_apartment() {
		return id_apartment;
	}
	public void setId_apartment(int id_apartment) {
		this.id_apartment = id_apartment;
	}
	public String getName_apartment() {
		return name_apartment;
	}
	public void setName_apartment(String name_apartment) {
		this.name_apartment = name_apartment;
	}
	public String getAdd_apartment() {
		return add_apartment;
	}
	public void setAdd_apartment(String add_apartment) {
		this.add_apartment = add_apartment;
	}
	public int getNumber_of_floor() {
		return number_of_floor;
	}
	public void setNumber_of_floor(int number_of_floor) {
		this.number_of_floor = number_of_floor;
	}

	
	
	
	
}
